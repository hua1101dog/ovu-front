import request from "@/utils/request";

export function findPage(query) {
  return request({
   
    url: "/ovu-university/university/inform/page",
    method: "get",
    params: query
  });
}


export function findDetail(id) {
  return request({
    url: `/ovu-university/university/inform/detail?informId=${id}`,
    method: "get"
  });
}

